public class Ficha {
    
    private int num;
    private String nombre;
    
    public Ficha(int num, String nombre){
        this.num = num;
        this.nombre = nombre;
    }

    public int getNum() {
        return num;
    }

    public String getNombre() {
        return nombre;
    }
    
    
    
    
    
}
